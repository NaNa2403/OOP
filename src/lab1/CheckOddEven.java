package lab1;

public class CheckOddEven {
    public static void main(String[] args) {
        int number = 49;
        System.out.println("Number: " + number);

        if(number % 2 == 0) {
            System.out.println("The number is even");
        }
        else{
            System.out.println("The number is odd");
        }
    }
}
